#include <stdio.h>
	
int main(void)
{
	int i;
	int num;
	int sum = 0;
	double avg;
	
	printf("10個の整数値(-9999〜9999)を入力\n");
	
	for(i=0; i<10; i++){
		scanf("%d",&num);
		sum += num;
	}
	
	avg = sum/10.0;
	
	printf("合計は%dです。\n",sum);
	printf("平均は%.2fです。\n",avg);
		
	return 0;
}
